#STRING UPPER AND LOWER CASE
a = "HElLo BoB"
b = a.lower()
c = a.upper()
print(f"NORMAL STRING: {a}")
print(f"LOWER-CASE STRING: {b}")
print(f"UPPER-CASE STRING: {c}")
#TO CHECK WHETHER IN UPPER OR LOWER CASE
print("")
print("TO CHECK IF UPPER CASE")
print(a.isupper())
print(b.isupper())
print(c.isupper())
print("TO CHECK IF LOWER CASE")
print(a.islower())
print(b.islower())
print(c.islower())